package array14;

public class SingleArray
{    
	 public static void main(String args[])
	 {    
	    //declaration, instantiation and initialization of an array  
	    int a[]={33,3,4,5};  
	    //traversing array    
	    for(int i=0;i<a.length;i++)//length is the property of array    
	        System.out.println(a[i]);    
	 }  
}   